    /**
     * 最大边等比例缩放图片
     *
     * @param src      the src
     * @param maxWidth the max width 最大边宽度
     * @param force    the force 是否强制进行缩放（强制输出maxWidth宽度）
     * @return bitmap bitmap
     */
    public static Bitmap scaleEqualRatioMax(Bitmap src, int maxWidth, boolean force) {
        if (src == null || maxWidth <= 0) {
            return null;
        }
        // 记录src的宽高
        float width = src.getWidth();
        float height = src.getHeight();
        // 创建一个matrix容器
        Matrix matrix = new Matrix();
        float scaleRatio = 1.0f;
        if (width >= height) {
            scaleRatio = maxWidth / width;
        } else {
            scaleRatio = maxWidth / height;
        }
        //如果源图高宽都小于maxWidth并且force为false,则不处理图片
        if ((!force)&&maxWidth>=width&&maxWidth>=height) {
            return src;
        }
        // 开始缩放
        matrix.postScale(scaleRatio, scaleRatio);
        // 创建缩放后的图片
        return Bitmap.createBitmap(src, 0, 0, (int) width, (int) height,
                matrix, true);
    }

